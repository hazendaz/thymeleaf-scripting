  @Select("sql/NameMapper/findById.sql")
  List<Name> findById(@Param("id") Integer id);

  @Select("sql/NameMapper/findByIdWithoutParamAnnotation.sql")
  List<Name> findByIdWithoutParamAnnotation(Integer id);

